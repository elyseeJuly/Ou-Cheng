// ── 查询接口 ───────────────────────────────────────────────
export function getTone(char: string, rhymeBook: 'ci_lin' | 'ping_shui' | 'xin_yun'): ToneClass | ToneClass[] {
  if (rhymeBook === 'ping_shui') {
    if (PING_SHUI_EXTRA[char] !== undefined) return PING_SHUI_EXTRA[char];
  }
  const result = XIN_YUN_MAP[char];
  if (!result) return 'P'; // 未知字默认平（宽松处理）
  return result;
}

export function getCanonicalTone(char: string, rhymeBook: 'ci_lin' | 'ping_shui' | 'xin_yun'): ToneClass {
  const t = getTone(char, rhymeBook);
  if (Array.isArray(t)) return t[0];
  return t;
}

export function isMultiTone(char: string): boolean {
  return Array.isArray(XIN_YUN_MAP[char]);
}
